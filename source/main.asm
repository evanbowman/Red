        INCLUDE "hardware.inc"


;;; ############################################################################

        SECTION "MAIN", ROM0


;;; ----------------------------------------------------------------------------

Main:
        ld	a, IEF_VBLANK	        ; vblank interrupt
	ld	[rIE], a	        ; setup

        ld	a, LCDCF_ON | LCDCF_BG8000 | LCDCF_BG9800 | LCDCF_OBJ16 | LCDCF_WINOFF | LCDCF_BGON
        ld	[rLCDC], a	        ; enable lcd
        ei

.loop:
        call    VBlankIntrWait

        jr      .loop



;;; ----------------------------------------------------------------------------


